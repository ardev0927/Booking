package com.jackpot.booking.models;

public class Seat {
    int x;
    int y;
    int pattern;
    int amount;
    boolean enable;

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getPattern() {
        return pattern;
    }

    public int getAmount() {
        return amount;
    }

    public boolean isEnable() {
        return enable;
    }
}
