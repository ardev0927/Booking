package com.jackpot.booking.models;

public class Product {
    private int id;
    private String title;
    private String picture;
    private int shop;
    private int category;

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getPicture() {
        return picture;
    }

    public int getShop() {
        return shop;
    }

    public int getCategory() {
        return category;
    }
}
